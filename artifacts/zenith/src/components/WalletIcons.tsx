export function pickIconForConnector(c: { id: string; name: string }) {
  const id = c.id.toLowerCase()
  const name = c.name.toLowerCase()
  if (id === 'metamask' || name.includes('metamask')) return MetaMaskIcon
  if (id === 'walletconnect' || name.includes('walletconnect')) return WalletConnectIcon
  if (id.includes('coinbase') || name.includes('coinbase')) return CoinbaseIcon
  if (name.includes('okx')) return OkxIcon
  if (name.includes('trust')) return TrustWalletIcon
  if (name.includes('rabby')) return RabbyIcon
  if (name.includes('phantom')) return PhantomIcon
  return GenericWalletIcon
}
